bool checker(vector<int> &p, int k) {
    int n = p.size();
    for (int i = 0; i < n; i++) {
        k -= p[i] % (i + 1) == 0;
    }
    return k == 0;
}

mt19937_64 rng(chrono::steady_clock::now().time_since_epoch().count());
int rand(int a, int b) { return uniform_int_distribution<int>(a, b)(rng); }

void solve() {
    int n, k;
    scan(n, k);
    vector<int> p(n);
    iota(p.begin(), p.end(), 1);
    if (k < n - 1) {
        rotate(p.begin() + k, p.begin() + k + 1, p.end());
    }
    else if (k == n - 1) {
        swap(p[0], p[1]);
    }
    print(p);
}

