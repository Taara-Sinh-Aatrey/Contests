int get(ll index) 
{ 
    ll sum = 0; // Iniialize result 
  
    // index in BIT[] is 1 more than the index in arr[] 
    index = index + 1; 
  
    // Traverse ancestors of BIT[index] 
    while (index>0) 
    { 
        // Add current element of BITree to sum 
        sum ^= BIT[index]; 
  
        // Move index to parent node in getSum View 
        index -= index & (-index); 
    } 
    return sum; 
} 
  
// Updates a node in Binary Index Tree (BIT) at given index 
// in BIT. The given value 'val' is added to BIT[i] and  
// all of its ancestors in tree. 
void upd(ll index, ll val) 
{ 
    // index in BIT[] is 1 more than the index in arr[] 
    index = index + 1; 
  
    // Traverse all ancestors and add 'val' 
    while (index <= n) 
    { 
	    // Add 'val' to current node of BI Tree 
	    BIT[index] ^= val; 
  
	    // Update index to that of parent in update View 
	    index += index & (-index); 
    } 
} 

