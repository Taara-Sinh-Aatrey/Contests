using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

vector<Mint> fact(1, 1);
vector<Mint> inv_fact(1, 1);

Mint C(int n, int k) {
    if (k < 0 || k > n) {
        return 0;
    }
    while ((int) fact.size() < n + 1) {
        fact.push_back(fact.back() * (int) fact.size());
        inv_fact.push_back(1 / fact.back());
    }
    return fact[n] * inv_fact[k] * inv_fact[n - k];
}

void solve() {
    
    int n;
    cin >> n;
    
    vector<vector<int>> g(n, vector<int>(n, INF));
    for(int i = 0; i < n - 1; i++) {
        int u, v;
        cin >> u >> v, --u, --v;
        g[u][v] = g[v][u] = 1;
    }
    
    for(int i = 0; i < n; i++) {
        g[i][i] = 0;
    }
    
    for(int k = 0; k < n; k++) {
        for(int i = 0; i < n; i++) {
            for(int j = 0; j < n; j++) {
                amin(g[i][j], g[i][k] + g[k][j]);
            }
        }
    }
    
    vector<vector<Mint>> dp(n + 1, vector<Mint>(n + 1));
    for(int i = 0; i <= n; i++) {
        for(int j = 0; j <= n; j++) {
            if(i == 0 && j == 0) {
                dp[i][j] = 0;
                continue;
            }
            if(j == 0) {
                dp[i][j] = 1;
                continue;
            }
            if(i == 0) {
                dp[i][j] = 0;
                continue;
            }
            dp[i][j] = (dp[i][j - 1] + dp[i - 1][j]) / 2;
        }
    }
    
    Mint ans = 0;
    
    for(int i = 0; i < n; i++) {
        for(int j = i + 1; j < n; j++) {
            for(int k = 0; k < n; k++) {
                int x = g[k][i], y = g[k][j];
                int d = (x + y - g[i][j]) / 2;
                x -= d;
                y -= d;
                ans += dp[x][y];
            }
        }
    }
    
    cout << ans / n << '\n';
}

