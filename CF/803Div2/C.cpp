#define int int64_t
const int inf = 1e18L + 5, mod = 1e9 + 7, N = 2e5 + 5;

void solve() {
    int n;
    scan(n);
    vector<int> a(n);
    scan(a);
    int zero = 0;
    int pos = 0, neg = 0;
    for (auto &x : a) {
        pos += x > 0;
        neg += x < 0;
        zero += x == 0;
    }
    if (max(pos, neg) + !!zero >= 3) {
        print("NO");
        return;
    }
    vector<int> b;
    for (auto &x : a) {
        if (x != 0 || zero <= 2) {
            b.emplace_back(x);
        }
        if (x == 0) {
            zero--;    
        }
    }
    a = b;
    n = a.size();
    sort(a.begin(), a.end());
    for (int i = 0; i < n; i++) {
        for (int j = i + 1; j < n; j++) {
            for (int k = j + 1; k < n; k++) {
                if (!binary_search(a.begin(), a.end(), a[i] + a[j] + a[k])) {
                    print("NO");
                    return;
                }
            }
        }
    }
    print("YES");
}

signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int t = 1;
    scan(t);
    for (int tt = 1; tt <= t; tt++) {
        solve();
    }
    return 0;
}