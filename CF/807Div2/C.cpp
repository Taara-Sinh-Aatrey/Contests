void solve() {
    int n, c, q;
    scan(n, c, q);
    string s;
    scan(s);
    vector<pair<int, int>> pairs{{0, n - 1}};
    while (c--) {
        int l, r;
        scan(l, r), --l, --r;
        pairs.emplace_back(l, r);
    }
    function<char(int)> lookup = [&] (int k) -> char {
        if (k < n) {
            return s[k];
        }
        int offset = 0;
        for (auto &[l, r] : pairs) {
            if (k <= offset + r - l) {
                return lookup(l + k - offset);
            }
            offset += r - l + 1;
        }
        assert(false);
    };
    while (q--) {
        int i;
        scan(i), --i;
        char ch = lookup(i);
        print(ch);
    }
}

signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int t = 1;
    scan(t);
    for (int tt = 1; tt <= t; tt++) {
        solve();
    }
    return 0;
}