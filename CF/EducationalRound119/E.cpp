#define int int64_t
const int inf = 1e18L + 5, mod = 1e9 + 7, N = 5e5 + 5;

void solve() {
    int q;
    scan(q);
    int n = 0;
    vector<vector<int>> idx(N);
    while (q--) {
        int t;
        scan(t);
        if (t == 1) {
            int x;
            scan(x);
            idx[x].emplace_back(n++);
        }
        else {
            int x, y;
            scan(x, y);
            if (x == y) continue;
            if (idx[x].size() > idx[y].size()) {
                swap(idx[x], idx[y]);
            }
            idx[y].insert(idx[y].end(), idx[x].begin(), idx[x].end());
            idx[x].clear();  
        }
    }
    vector<int> a(n);
    for (int x = 1; x < N; x++) {
        for (auto &i : idx[x]) {
            a[i] = x;
        }
    }
    print(a);
}

signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int t = 1;
    for (int tt = 1; tt <= t; tt++) {
        solve();
    }
    return 0;
}