#define int int64_t
const int inf = 1e18L + 5, mod = 1e9 + 7, N = 2e5 + 5;

vector<int> bitwiseEquations(vector<int> a, vector<int> b) {
    int n = a.size();
    vector<int> ans(n, 0);
    for(int i = 0; i < n; i++) {
        if(a[i] % 2 != b[i] % 2)  continue;
        int AND = (a[i] - b[i]) / 2;
        int XOR = b[i];
        if(AND & XOR) continue;
        int x = AND;
        int y = AND + XOR;
        ans[i] = 2 * x + 3 * y;
    }
    return ans;
}

