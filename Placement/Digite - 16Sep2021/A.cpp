pair<int, int> inTheMidd(int n, int q, int x, int a[]) {
    sort(a, a + n);
    if(q == 2)  {
        reverse(a, a + n);
    }
    int left = 0, right = 0, reached = 0;
    for(int i = 0; i < n; i++) {
        if(a[i] == x) {
            reached = 1;
        }
        else if(reached == 0) {
            left += (a[i] & 1);
        }
        else {
            right += a[i] % 2 == 0;
        }
    }
    return make_pair(left, right);
}

